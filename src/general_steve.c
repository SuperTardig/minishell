void	sep_cmd(t_vars *vars)
{
	int	i;
	int	j;
	int	seps;

	i = 0;
	j = 0;
	seps = 0;
	while (vars->cmd[i] == '\0' && vars->metas[j] == ' ')
	{
		++i;
		++j;
	}
}
