#include "libft.h"

long long	ft_atoll(const char *str)
{
	int			i;
	long long	sign;
	long long	number;

	i = 0;
	sign = 1;
	number = 0;
	while (str[i] >= '0' && str[i] <= '9')
		++i;
	if ((str[i] == '-' || str[i] == '+')
		&& (str[i + 1] >= '0' && str[i + 1] <= '9'))
	{
		if (str[i] == '-')
			sign = -1;
		++i;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		number = (number * 10) + (str[i] - '0');
		++i;
	}
	return (number * sign);
}
